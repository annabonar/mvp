	<h3>Story behind Times12px</h3>
		<p class='spacing'>Often times, students are told to format documents using 12px font. But 12px is not metrically consistent across different typefaces. A 12px Verdana versus a 12px Garamond, for example, will take up different amounts of space even though both are 12px.
		 </p>

		 <p class='spacing'>What professors really mean when they say 12px is 12px Times New Roman. With that in mind, this project tries to 'convert' different typefaces so that they are metrically (in simple terms, spacially) equivalent to 12px Times New Roman. Hence the name, Times12px.
		 </p>

		 <p class='spacing'>Students can now easily refer to this website to see how they can adjust the size of a typeface to match that of 12px Times New Roman. What's more, all the typefaces listed here have <em>italic</em> and <strong>bold</strong> styles, which is not only convenient, but necessary for formatting citations.
		 </p>

		 <p class='spacing'>These typefaces are all highly readable, professional, and are great alternatives to the usual Times New Roman. Enjoy browsing!
		 </p>

